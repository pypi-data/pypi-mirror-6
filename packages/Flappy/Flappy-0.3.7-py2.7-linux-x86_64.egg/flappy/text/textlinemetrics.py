
class TextLineMetrics(object):

    def __init__(self, x=0.0, width=0.0, height=0.0, 
                    ascent=0.0, descent=0.0, leading=0.0):
        self.x = x
        self.width = width
        self.height = height
        self.ascent = ascent
        self.descent = descent
        self.leading = leading
